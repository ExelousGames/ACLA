"""Training provider abstractions for telemetry LLM fine-tuning."""

from __future__ import annotations

import asyncio
import json
from dataclasses import dataclass, field
from pathlib import Path
from typing import Any, Dict, List, Optional

from ..telemetry_prompt_dataset_builder import (
	TelemetryPromptDatasetBuilder,
	DatasetBuildStats,
)


@dataclass
class LLMTrainingContext:
	"""Context shared with all training providers for a single run.
		output_root: is the base directory where providers should write their datasets.
  	"""

	dataset_id: str
	segments_cache_key: str
	output_root: Path
	data_caching_service: Any
	annotations: Optional[Dict[str, Dict[str, Any]]] = None
	shuffle: bool = True
	metadata: Dict[str, Any] = field(default_factory=dict)


@dataclass
class LLMTrainingContribution:
	"""Dataset artifact generated by a training provider."""

	provider_name: str
	dataset_path: Path
	stats: Dict[str, Any] = field(default_factory=dict)
	metadata: Dict[str, Any] = field(default_factory=dict)
	cleanup: bool = True


@dataclass
class PromptResponseExample:
	"""Normalized prompt/response payload generated by providers."""

	prompt: str
	response: str
	system_prompt: Optional[str] = None
	metadata: Dict[str, Any] = field(default_factory=dict)

	def to_record(self) -> Dict[str, Any]:
		record = {
			"prompt": self.prompt,
			"response": self.response,
		}
		if self.system_prompt:
			record["system_prompt"] = self.system_prompt
		if self.metadata:
			record["metadata"] = self.metadata
		return record


class BaseLLMTrainingProvider:
	"""Interface for providers contributing prompt/response samples."""

	name: str = "base_provider"

	async def produce(self, context: LLMTrainingContext) -> Optional[LLMTrainingContribution]:
		"""Generates a dataset contribution based on the provided context."""
		raise NotImplementedError


class SegmentExplanationTrainingProvider(BaseLLMTrainingProvider):
	"""Wraps the legacy telemetry segment builder so it can take part in orchestration."""

	name = "segment_explanation"

	ANNOTATION_SUFFIX = ".annotations.jsonl"

	def __init__(self, builder: TelemetryPromptDatasetBuilder) -> None:
		self._builder = builder

	async def produce(self, context: LLMTrainingContext) -> Optional[LLMTrainingContribution]:
		if not context.segments_cache_key:
			return None

		output_dir = context.output_root / self.name
		output_dir.mkdir(parents=True, exist_ok=True)
		dataset_path = output_dir / f"{context.dataset_id}_{self.name}.jsonl"

		def _build_dataset() -> DatasetBuildStats:

			with dataset_path.open("w", encoding="utf-8") as handle:
				def consume(record: Dict[str, Any]) -> None:
					handle.write(
						json.dumps(
							record,
							ensure_ascii=False,
							default=self._builder._json_default,  # type: ignore[attr-defined]
						)
						+ "\n"
					)

				_, stats = self._builder.build_from_cached_segments(
					segments_cache_key=context.segments_cache_key,
					shuffle=context.shuffle,
					record_consumer=consume,
				)
			return stats

		stats = await asyncio.to_thread(_build_dataset)

		annotation_summary = await asyncio.to_thread(self._apply_annotations_to_dataset, dataset_path)
		annotated_examples = annotation_summary.get("annotated_examples", 0)
		total_examples = annotation_summary.get("total_examples", stats.windows_kept)

		if total_examples and annotated_examples == 0:
			raise RuntimeError(
				f"No telemetry annotations were found for {dataset_path}. Run the telemetry_prompt_annotation_app "
				"to annotate the generated dataset before training."
			)

		if annotation_summary.get("pending_examples"):
			print(
				f"[WARN] Only {annotated_examples}/{total_examples} telemetry windows have annotations. "
				"Unannotated windows will be skipped during training."
			)

		if stats.windows_kept == 0:
			dataset_path.unlink(missing_ok=True)
			return None

		stats_dict = stats.to_dict()
		stats_dict.update({
			"provider": self.name,
			"total_examples": total_examples,
			"annotated_examples": annotated_examples,
			"annotation_coverage": annotation_summary.get("annotation_coverage", 0.0),
			"output_path": str(dataset_path),
		})

		return LLMTrainingContribution(
			provider_name=self.name,
			dataset_path=dataset_path,
			stats=stats_dict,
			metadata={
				"builder": "telemetry_segments",
				"annotations": annotation_summary,
			},
		)

	def _apply_annotations_to_dataset(self, dataset_path: Path) -> Dict[str, Any]:
		"""Merge saved annotations into freshly built dataset records."""
		annotation_path = dataset_path.with_suffix(dataset_path.suffix + self.ANNOTATION_SUFFIX)
		annotations: Dict[str, Dict[str, Any]] = {}
		if annotation_path.exists():
			with annotation_path.open("r", encoding="utf-8") as annotation_file:
				for line in annotation_file:
					line = line.strip()
					if not line:
						continue
					try:
						payload = json.loads(line)
					except json.JSONDecodeError:
						continue
					window_id = payload.get("window_id")
					if not window_id:
						continue
					explanation = payload.get("coaching_explanation", "")
					if isinstance(explanation, str):
						annotations[window_id] = {
							"coaching_explanation": explanation,
							"updated_at": payload.get("updated_at"),
						}

		updated_records: List[Dict[str, Any]] = []
		total_examples = 0
		annotated_examples = 0
		annotations_applied = 0
		pending_examples = 0
		changed = False
		json_default = getattr(self._builder, "_json_default", None)

		with dataset_path.open("r", encoding="utf-8") as dataset_file:
			for line in dataset_file:
				line = line.strip()
				if not line:
					continue
				try:
					record = json.loads(line)
				except json.JSONDecodeError:
					continue

				metadata = record.get("metadata")
				if not isinstance(metadata, dict):
					metadata = {}
					record["metadata"] = metadata

				existing_annotation = metadata.get("annotation") if isinstance(metadata.get("annotation"), dict) else None
				window_id = metadata.get("window_id")

				existing_response = record.get("response")
				explanation = existing_response if isinstance(existing_response, str) else ""
				if explanation:
					explanation = explanation.strip()

				if not explanation and existing_annotation:
					candidate = existing_annotation.get("coaching_explanation")
					if isinstance(candidate, str) and candidate.strip():
						explanation = candidate.strip()

				if not explanation and window_id and window_id in annotations:
					store_payload = annotations[window_id]
					explanation = (store_payload.get("coaching_explanation") or "").strip()
					if explanation:
						annotations_applied += 1

				is_annotated = bool(explanation)
				if is_annotated:
					annotated_examples += 1
				else:
					pending_examples += 1

				if (existing_response or "") != (explanation or ""):
					record["response"] = explanation or ""
					changed = True
				elif "response" not in record:
					record["response"] = explanation or ""
					changed = True

				if metadata.get("annotation_complete") != is_annotated:
					metadata["annotation_complete"] = is_annotated
					changed = True

				if is_annotated:
					annotation_meta = metadata.setdefault("annotation", {})
					if annotation_meta.get("coaching_explanation") != explanation:
						annotation_meta["coaching_explanation"] = explanation
						changed = True
					if window_id and window_id in annotations:
						updated_at = annotations[window_id].get("updated_at")
						if updated_at and annotation_meta.get("updated_at") != updated_at:
							annotation_meta["updated_at"] = updated_at
							changed = True
				elif existing_annotation and existing_annotation.get("coaching_explanation"):
					existing_annotation["coaching_explanation"] = ""
					changed = True

				if "explanation" in record:
					record.pop("explanation", None)
					changed = True

				metadata.setdefault("window_id", window_id)
				updated_records.append(record)
				total_examples += 1

		if changed:
			with dataset_path.open("w", encoding="utf-8") as dataset_file:
				for record in updated_records:
					if json_default:
						dataset_file.write(json.dumps(record, ensure_ascii=False, default=json_default) + "\n")
					else:
						dataset_file.write(json.dumps(record, ensure_ascii=False) + "\n")

		return {
			"annotation_path": str(annotation_path) if annotation_path.exists() else None,
			"annotation_store_found": bool(annotations),
			"annotations_available": len(annotations),
			"annotations_applied": annotations_applied,
			"total_examples": total_examples,
			"annotated_examples": annotated_examples,
			"pending_examples": pending_examples,
			"annotation_coverage": (annotated_examples / total_examples) if total_examples else 0.0,
		}


class TransformerDirectiveTrainingProvider(BaseLLMTrainingProvider):
	"""Generates samples that teach the LLM how to delegate to the transformer model."""

	name = "transformer_directive"

	def __init__(
		self,
		builder: TelemetryPromptDatasetBuilder,
		*,
		max_samples: int = 1500,
	) -> None:
		self._builder = builder
		self._max_samples = max_samples
		self._system_prompt = (
			"You are an elite telemetry strategist. When a segment requires fine-grained action "
			"planning, wrap the transformer invocation in the response so downstream services can "
			"delegate execution. Always include a concise coaching summary alongside the directive."
		)

	async def produce(self, context: LLMTrainingContext) -> Optional[LLMTrainingContribution]:
		if not context.segments_cache_key:
			return None

		output_dir = context.output_root / self.name
		output_dir.mkdir(parents=True, exist_ok=True)
		dataset_path = output_dir / f"{context.dataset_id}_{self.name}.jsonl"

		total_examples = 0
		segments_considered = 0

		def _generate_examples() -> None:
			nonlocal total_examples, segments_considered

			with dataset_path.open("w", encoding="utf-8") as handle:
				chunk_iterator = context.data_caching_service.get_cached_data_chunks(
					cache_key=context.segments_cache_key
				)
				prediction_steps = self._builder.config.prediction_steps

				for chunk_df in chunk_iterator:
					if chunk_df is None or chunk_df.empty:
						continue

					for raw_record in chunk_df.to_dict("records"):
						segment_groups = self._builder._resolve_segments_from_record(raw_record)  # type: ignore[attr-defined]
						if not segment_groups:
							continue

						for segment in segment_groups:
							segments_considered += 1
							if len(segment) < self._builder.config.context_steps:
								continue

							context_slice = segment[: self._builder.config.context_steps]
							if not context_slice:
								continue

							future_slice = segment[
								self._builder.config.context_steps : self._builder.config.context_steps + prediction_steps
							]
							directive = self._build_directive(
								context_slice,
								future_slice,
								context.metadata,
							)
							example = self._build_entry(
								context_slice,
								directive,
								prediction_steps,
								context.metadata,
							)
							handle.write(
							json.dumps(
								example.to_record(),
								ensure_ascii=False,
								default=self._builder._json_default,  # type: ignore[attr-defined]
							)
							+ "\n"
						)
							total_examples += 1

							if self._max_samples and total_examples >= self._max_samples:
								return

		await asyncio.to_thread(_generate_examples)

		if total_examples == 0:
			dataset_path.unlink(missing_ok=True)
			return None

		stats = {
			"provider": self.name,
			"total_examples": total_examples,
			"segments_considered": segments_considered,
			"output_path": str(dataset_path),
		}

		return LLMTrainingContribution(
			provider_name=self.name,
			dataset_path=dataset_path,
			stats=stats,
			metadata={
				"directive": "transformer",
				"segments_considered": segments_considered,
			},
		)

	def _build_directive(
		self,
		context_window: Any,
		future_slice: Any,
		metadata: Dict[str, Any],
	) -> Dict[str, Any]:
		control_features = ("Physics_gas", "Physics_brake", "Physics_steer_angle", "Physics_gear")

		max_brake = max((step.get("Physics_brake", 0.0) or 0.0) for step in context_window)
		max_gas = max((step.get("Physics_gas", 0.0) or 0.0) for step in context_window)
		steer_variance = self._variance([step.get("Physics_steer_angle", 0.0) or 0.0 for step in context_window])
		speed_values = [step.get("Physics_speed_kmh", 0.0) or 0.0 for step in context_window]
		speed_drop = max(speed_values) - min(speed_values) if speed_values else 0.0

		if max_brake > 0.7 and speed_drop > 20:
			reason = "Heavy braking zone detected – delegate precise deceleration plan to transformer."
		elif steer_variance > 100:
			reason = "Rapid steering oscillations – transformer should compute fine-grained steering sequence."
		elif max_gas > 0.95 and speed_drop < 5:
			reason = "Flat-out acceleration – transformer can optimise throttle modulation to avoid wheel spin."
		else:
			reason = "Mixed-control segment – transformer needed for coordinated pedal and steering adjustments."

		directive = {
			"model": "expert_action_transformer",
			"reason": reason,
			"expected_outputs": list(control_features),
			"future_hint": future_slice[:1] if future_slice else [],
			"metadata": metadata or {},
		}
		return directive

	def _build_entry(
		self,
		context_window: Any,
		directive: Dict[str, Any],
		prediction_steps: int,
		metadata: Dict[str, Any],
	) -> PromptResponseExample:
		user_prompt = (
			"Task: Review the telemetry segment and respond with a JSON object that contains a coaching summary "
			"and a `transformer_directive`. Use the directive to describe how the ExpertActionTransformer should "
			"be invoked for high-resolution planning. Include any crucial parameters it must receive.\n\n"
			"Telemetry context (ordered timesteps):\n"
			f"{json.dumps(context_window, indent=2, ensure_ascii=False)}\n\n"
			f"Future horizon hint ({prediction_steps} steps available): {prediction_steps}"
		)

		assistant_payload = {
			"coaching_summary": directive["reason"],
			"transformer_directive": {
				"action": "invoke",
				"model": directive["model"],
				"inputs": {
					"reuse_context_window": True,
					"expected_outputs": directive["expected_outputs"],
					"future_hint": directive.get("future_hint", []),
				},
				"metadata": directive.get("metadata", {}),
			},
		}

		return PromptResponseExample(
			prompt=user_prompt,
			response=json.dumps(assistant_payload, ensure_ascii=False),
			system_prompt=self._system_prompt,
			metadata={
				"provider": self.name,
				"prediction_steps": prediction_steps,
				"source": metadata,
			},
		)

	@staticmethod
	def _variance(values: Any) -> float:
		if not values:
			return 0.0
		mean = sum(values) / len(values)
		return float(sum((value - mean) ** 2 for value in values) / len(values))


